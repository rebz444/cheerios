# ── 10b. Per-insertion physiological vs histological cortex–STR boundary ──────
# For each STR-probe insertion, find the depth where most units transition
# from "high FR" (cortex) to "low FR" (striatum) — that is the physiological
# cortex–STR boundary. Compare to where atlas labels switch from cortex →
# striatum (the histological boundary). A large offset is a sanity-check
# failure on the depth gate downstream uses (1500 µm in 0h).
print("\n── Per-insertion cortex–STR boundary (physiology vs histology) ──")

_BND_FR_THRESHOLD = 5.0   # Hz — units below this are likely MSNs / low-FR cells

_CTX_PREFIXES_BND = ("MOp", "MOs", "VIS", "SSp", "SSs", "ACA", "PL", "ILA", "ORB", "AI", "RSP")
_STR_REGIONS_BND  = {"CP", "STR", "STRd", "STRv", "ACB", "OT"}


def _is_ctx(r):
    r = str(r)
    return any(r.startswith(px) for px in _CTX_PREFIXES_BND)


def _is_str(r):
    return str(r) in _STR_REGIONS_BND


# Use the unfiltered (pre-EXCLUDE_REGIONS) frame so we have all sentinels;
# but boundary detection only cares about real placed units, so use up_df.
_str_units_bnd = up_df[up_df["probe_region"] == "str"].copy()
_str_units_bnd["insertion_key"] = (
    _str_units_bnd["mouse"] + "_" +
    _str_units_bnd["date_only"] + "_" +
    _str_units_bnd["insertion_number"].astype(str)
)

# Detect FR column (already on up_df from 0d).
_FR_COL_BND = next(
    (c for c in ["firing_rate", "mean_firing_rate", "fr", "spike_rate"]
     if c in _str_units_bnd.columns),
    None,
)

boundary_rows = []
if _FR_COL_BND and "peak_channel_depth" in _str_units_bnd.columns:
    for ins_key, ins_df in _str_units_bnd.groupby("insertion_key"):
        if len(ins_df) < 10:
            continue
        ins_df = ins_df.sort_values("peak_channel_depth")
        depths = ins_df["peak_channel_depth"].to_numpy()
        frs    = ins_df[_FR_COL_BND].to_numpy()
        mask   = ~np.isnan(depths) & ~np.isnan(frs)
        if mask.sum() < 10:
            continue
        depths = depths[mask]
        frs    = frs[mask]

        # Sliding window: fraction of low-FR units. First crossing of 0.5
        # ≈ cortex-STR boundary in physiology.
        win = max(3, len(depths) // 10)
        is_low = (frs < _BND_FR_THRESHOLD).astype(float)
        low_frac = np.array([
            is_low[max(0, i - win):min(len(is_low), i + win)].mean()
            for i in range(len(is_low))
        ])
        cross = np.where(low_frac > 0.5)[0]
        bnd_phys = float(depths[cross[0]]) if len(cross) else np.nan

        # Histology: first crossing of cortex-fraction below 0.5.
        is_ctx_arr = ins_df.loc[mask, "region_acronym"].apply(_is_ctx).to_numpy().astype(float)
        ctx_frac = np.array([
            is_ctx_arr[max(0, i - win):min(len(is_ctx_arr), i + win)].mean()
            for i in range(len(is_ctx_arr))
        ])
        cross_ctx = np.where(ctx_frac < 0.5)[0]
        bnd_hist = float(depths[cross_ctx[0]]) if len(cross_ctx) else np.nan

        n_ctx = int(is_ctx_arr.sum())
        n_str = int(ins_df.loc[mask, "region_acronym"].apply(_is_str).sum())
        boundary_rows.append({
            "mouse":            ins_df["mouse"].iloc[0],
            "date_only":        ins_df["date_only"].iloc[0],
            "insertion_number": int(ins_df["insertion_number"].iloc[0]),
            "n_units":          int(mask.sum()),
            "n_cortex":         n_ctx,
            "n_striatum":       n_str,
            "pct_cortex":       round(100 * n_ctx / mask.sum(), 1),
            "pct_striatum":     round(100 * n_str / mask.sum(), 1),
            "boundary_physiology_um": round(bnd_phys, 0) if not np.isnan(bnd_phys) else np.nan,
            "boundary_histology_um":  round(bnd_hist, 0) if not np.isnan(bnd_hist) else np.nan,
        })

boundary_df = pd.DataFrame(boundary_rows)
if len(boundary_df):
    boundary_df["offset_um"] = (
        boundary_df["boundary_histology_um"] - boundary_df["boundary_physiology_um"]
    )
    print(f"  Insertions analysed: {len(boundary_df)}")
    _phys = boundary_df["boundary_physiology_um"].dropna()
    _hist = boundary_df["boundary_histology_um"].dropna()
    if len(_phys):
        print(f"    Physiological boundary (FR<{_BND_FR_THRESHOLD} Hz crossover): "
              f"{_phys.mean():.0f} ± {_phys.std():.0f} µm  (n={len(_phys)})")
    if len(_hist):
        print(f"    Histological boundary  (cortex→STR label flip)        : "
              f"{_hist.mean():.0f} ± {_hist.std():.0f} µm  (n={len(_hist)})")
    if len(_phys) and len(_hist):
        _off = boundary_df["offset_um"].dropna()
        print(f"    Median offset (histology − physiology): {_off.median():.0f} µm")
        if abs(_off.median()) > 300:
            print(f"    *** WARNING: large offset — review shrinkage correction or "
                  f"the 1500 µm depth gate in 0h ***")

    boundary_path = PLOT_DIR / "boundary_phys_vs_hist.csv"
    boundary_df.to_csv(boundary_path, index=False)
    print(f"  Saved → {boundary_path}")

    # Plot: scatter of physiological vs histological boundary + offset histogram.
    _valid = boundary_df["boundary_physiology_um"].notna() & boundary_df["boundary_histology_um"].notna()
    if _valid.sum() >= 3:
        fig, axes = plt.subplots(1, 2, figsize=(13, 5))
        ax = axes[0]
        ax.scatter(boundary_df.loc[_valid, "boundary_physiology_um"],
                   boundary_df.loc[_valid, "boundary_histology_um"],
                   s=55, alpha=0.75, c="#2166AC", edgecolors="white")
        _lo = float(min(boundary_df.loc[_valid, "boundary_physiology_um"].min(),
                        boundary_df.loc[_valid, "boundary_histology_um"].min())) - 200
        _hi = float(max(boundary_df.loc[_valid, "boundary_physiology_um"].max(),
                        boundary_df.loc[_valid, "boundary_histology_um"].max())) + 200
        ax.plot([_lo, _hi], [_lo, _hi], "k--", linewidth=1, label="y = x")
        ax.set_xlim(_lo, _hi); ax.set_ylim(_lo, _hi)
        ax.set_aspect("equal")
        ax.set_xlabel("Physiological boundary (µm)\nFR-based crossover")
        ax.set_ylabel("Histological boundary (µm)\nlabel-based crossover")
        ax.set_title("Cortex–STR boundary — physiology vs histology")
        ax.grid(True, alpha=0.2); ax.legend()

        ax = axes[1]
        _off = boundary_df["offset_um"].dropna()
        if len(_off):
            ax.hist(_off, bins=15, color="#2166AC", edgecolor="white", alpha=0.75)
            ax.axvline(0, color="red", linestyle="--", label="No offset")
            ax.axvline(_off.median(), color="orange",
                       label=f"Median: {_off.median():.0f} µm")
            ax.set_xlabel("Offset (histology − physiology) (µm)")
            ax.set_ylabel("Insertion count")
            ax.set_title("Boundary offset distribution")
            ax.legend()

        fig.suptitle("Per-insertion cortex–STR boundary check", fontsize=12, fontweight="bold")
        fig.tight_layout()
        fig.savefig(PLOT_DIR / "boundary_phys_vs_hist.png", dpi=200, bbox_inches="tight")
        plt.close(fig)
        print(f"  Saved → boundary_phys_vs_hist.png")
else:
    print("  Skipped — need firing_rate + peak_channel_depth and ≥10 units / insertion.")


