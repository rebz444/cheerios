# ── 3. Map units to brain regions ─────────────────────────────────────────────
# Method: per-session tip-anchored with DV shrinkage correction.
#   peak_ch_fixed     = peak_channel_depth * DV_SCALE[mouse]
#   session_tip_fixed = min(insertion_depth_um * DV_SCALE[mouse], trace_max)
#   dist              = session_tip_fixed − peak_ch_fixed
# When the recording log has no depth for the session, session_tip_fixed falls
# back to trace_max. The cap protects against extrapolating past the tracked
# region (e.g. probe inserted deeper than the dye trace was recovered).
# Both dist and track dist_um are in fixed-tissue space.
# Units above the per-session surface anchor are marked above_surface.

# Per-track lookup keyed by (mouse, track) — built from track_qc + the local files.
# Holds the path, status, and pre-loaded track DataFrame for each track.
track_info = {}
for _, t in track_qc.iterrows():
    key  = (t["mouse"], t["track"])
    path = find_track_csv(t["mouse"], t["track"])
    track_info[key] = dict(
        track_path=path,
        depth_status=t.get("depth_status", ""),
        passes_audit=bool(t.get("passes_audit", False)),
    )

print("\nCaching track files...")
track_cache = {}
for info in track_info.values():
    if info["track_path"] is not None and info["track_path"] not in track_cache:
        track_cache[info["track_path"]] = load_track_csv(info["track_path"])

print("Mapping units to brain regions (per-session tip-anchored, DV-scale corrected)...")

units_merged = up_df.merge(
    session_info[["mouse", "date_only", "insertion_number",
                  "region", "hemisphere", "depth_um", "track_file"]],
    on=["mouse", "date_only", "insertion_number"],
    how="left"
)

# Sanity check: warn if any (mouse, track_file) with units is missing from
# track_qc. Those units will be excluded via the no_track_file path below; we
# don't hard-fail because the missing CSV is usually a known re-tracing gap.
needed_tracks = set(
    (m, t) for m, t in zip(units_merged["mouse"], units_merged["track_file"])
    if isinstance(t, str) and t
)
unaudited = needed_tracks - audited_tracks
if unaudited:
    print(f"\nWARNING: {len(unaudited)} (mouse, track) pairs have units but no audit row "
          f"(units will be excluded as no_track_file): {sorted(unaudited)}")

region_acronyms = []
region_names    = []
dist_along_track = []
consistency_col  = []
track_file_col   = []
depth_confidence = []

for _, row in units_merged.iterrows():
    mouse      = row["mouse"]
    track_name = row.get("track_file")

    if not isinstance(track_name, str) or not track_name:
        region_acronyms.append("no_session_match")
        region_names.append("No session match")
        dist_along_track.append(np.nan)
        consistency_col.append("no match")
        track_file_col.append(None)
        depth_confidence.append("no match")
        continue

    info = track_info.get((mouse, track_name))

    peak_d = row["peak_channel_depth"]

    if isinstance(peak_d, float) and np.isnan(peak_d):
        region_acronyms.append("unknown_depth")
        region_names.append("Depth unknown")
        dist_along_track.append(np.nan)
        consistency_col.append(info["depth_status"] if info else "no match")
        track_file_col.append(track_name)
        depth_confidence.append("unknown")
        continue

    if info is None or info["track_path"] is None:
        region_acronyms.append("no_track_file")
        region_names.append("Track file not found")
        dist_along_track.append(np.nan)
        consistency_col.append("no match" if info is None else info["depth_status"])
        track_file_col.append(track_name)
        depth_confidence.append("no track")
        continue

    if not info["passes_audit"]:
        region_acronyms.append("track_audit_failed")
        region_names.append("Track failed 0e audit")
        dist_along_track.append(np.nan)
        consistency_col.append(info["depth_status"])
        track_file_col.append(track_name)
        depth_confidence.append("track_failed")
        continue

    tdf       = track_cache[info["track_path"]]
    trace_max = tdf["dist_um"].max()

    # Convert peak_channel_depth from in-vivo to fixed tissue space
    scale         = get_dv_scale(mouse)
    peak_d_fixed  = peak_d * scale

    # Tip anchor: per-session insertion_depth (scaled to fixed tissue), capped
    # at trace_max because we can't extrapolate beyond the tracked region.
    # Falls back to trace_max when the recording log has no depth for the session.
    session_depth_um = row.get("depth_um")
    if pd.isna(session_depth_um):
        session_tip_fixed = trace_max
    else:
        session_tip_fixed = min(session_depth_um, trace_max)

    dist = session_tip_fixed - peak_d_fixed

    acronym, full_name = lookup_region(tdf, dist)
    region_acronyms.append(acronym)
    region_names.append(full_name)
    dist_along_track.append(dist)
    consistency_col.append(info["depth_status"])
    track_file_col.append(track_name)

    # Confidence tier
    if mouse in CLEARING_PROBLEMATIC_MICE:
        conf = "low"
    elif acronym == "above_surface":
        conf = "above_surface"
    elif acronym in ("below_track", "no_track_file"):
        conf = "below_track"
    else:
        conf = "ok"
    depth_confidence.append(conf)

up_df["region_acronym"]      = region_acronyms
up_df["region_name"]         = region_names
up_df["dist_along_track_um"] = dist_along_track
up_df["track_file"]          = track_file_col
up_df["depth_consistency"]   = consistency_col
up_df["depth_confidence"]    = depth_confidence

print(f"  Done mapping {len(up_df):,} units")
print(f"\n  Depth confidence breakdown:")
print(up_df["depth_confidence"].value_counts().to_string())


