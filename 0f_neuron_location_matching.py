def get_dv_scale(mouse: str) -> float:
    """Return the DV shrinkage scale for a mouse, or raise with a helpful message."""
    if mouse not in DV_SCALE:
        raise KeyError(
            f"DV shrinkage missing for {mouse}. Re-run `python compute_dv_shrinkage.py` "
            f"to refresh {_DV_SCALE_CSV.name}."
        )
    return DV_SCALE[mouse]

