"""
0f_neuron_location_matching.py
==============================
Assigns brain region labels to sorted units using histological probe tracks.
Run AFTER 0e_track_qc.py — units belonging to tracks that fail the audit
are excluded from region assignment via the track_audit_failed sentinel.

Mapping method: per-session tip-anchored with DV tissue shrinkage correction.
  peak_ch_fixed     = peak_channel_depth * DV_SCALE[mouse]            (in-vivo → fixed tissue)
  session_tip_fixed = min(insertion_depth * DV_SCALE[mouse], trace_max)
  dist              = session_tip_fixed − peak_ch_fixed                (distance from surface in fixed tissue)
Falls back to trace_max only when the recording log has no depth for the session.

DV scale factors are loaded from dv_shrinkage.csv (one row per mouse).
That CSV is generated by compute_dv_shrinkage.py from each mouse's
brainreg boundaries.tiff. Missing entries raise here — never silently default.

Inputs:
  unit_properties_with_qc.csv  — unit properties + QC pass/fail flags (output of 0d)
  dv_shrinkage.csv             — per-mouse DV shrinkage (output of compute_dv_shrinkage.py)

Outputs:
  unit_properties_with_qc_and_regions.csv  — unit properties + QC flags + region assignments
"""

